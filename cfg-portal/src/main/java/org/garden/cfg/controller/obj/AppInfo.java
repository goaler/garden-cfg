package org.garden.cfg.controller.obj;

import java.util.Date;

public class AppInfo {
	private Integer appId;

	private String appCode;

	private String appName;

	private String comment;

	private Integer status;

	private String createUserCode;

	private Date createTime;

	private String updateUserCode;

	private Date updateTime;

	public Integer getAppId() {
		return appId;
	}

	public void setAppId(Integer appId) {
		this.appId = appId;
	}

	public String getAppCode() {
		return appCode;
	}

	public void setAppCode(String appCode) {
		this.appCode = appCode;
	}

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getCreateUserCode() {
		return createUserCode;
	}

	public void setCreateUserCode(String createUserCode) {
		this.createUserCode = createUserCode;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public String getUpdateUserCode() {
		return updateUserCode;
	}

	public void setUpdateUserCode(String updateUserCode) {
		this.updateUserCode = updateUserCode;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	@Override
	public String toString() {
		return "AppInfo [appId=" + appId + ", appCode=" + appCode + ", appName=" + appName + ", comment=" + comment
				+ ", status=" + status + ", createUserCode=" + createUserCode + ", createTime=" + createTime
				+ ", updateUserCode=" + updateUserCode + ", updateTime=" + updateTime + "]";
	}

}
